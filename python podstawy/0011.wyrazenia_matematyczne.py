num1 = 7
num2 = 2
num3 = num1 + num2 #+ dodawanie num3=9
num4 = num1 - num2 #- odejmowanie num4=5
num5 = num1 * num2 #* mnożenie num5=14
num6 = num1 / num2 #/ dzielenie num6=3.5
num7 = num1 ** num2 #** potęgowanie num7=49
num8 = num1 // num2 #// dzielenie (wynik int) num8=3
num9 = num1 % num2 #% reszta z dzielenia num9=1
num10= -num1 #(-) liczba przeciwna num10=-7
