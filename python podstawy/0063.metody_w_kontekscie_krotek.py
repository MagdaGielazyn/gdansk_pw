#• count()
#zwraca ile razy dana wartość pojawiła się w krotce
krotka = (1, 2, 4.5, True, 1,'‘a', 6, 'numer', 1)
licz = krotka.count(1)
print(licz)
#wynik: 4
#• index()
#zwraca indeks, na którym dana wartość pojawiła się w krotce
#krotka = (1, 2, 4.5, True, 1, ‘a’, 6, ‘numer’, 1)
licz = krotka.index(1)
print(licz)
#wynik: 0