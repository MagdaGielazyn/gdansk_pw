#Oblicz pierwiastek kwadratowy liczby podanej przez użytkownika używając biblioteki math
#Przykladowy output:
#Podaj liczbę: 3
#Pierwiastek kwadratowy liczby 3.0 to 1.7320508075688772

import math
num = int(input('Podaj liczbe: '))
pierwiastek = math.sqrt(num)

print(f'Pierwiastek kwadratowy z {num} to {pierwiastek}.')

